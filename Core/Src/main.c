void display() {
  for (uint8_t i = 0; i < 8; i++) {
    LED = 0x00;

    if (food.x == i) {
      LED |= (1 << food.y);
    }

    dot_t *self = ptr;
    while (self != 0) {
      if (self->x == i) {
        LED |= (1 << self->y);
      }
      self = self->next;
    }
    max7219(i + 1, LED);
  }
}

void generate_food() {
  dot_t *self = ptr;
GEN:
  food.x = HAL_RNG_GetRandomNumber(&hrng) % 8;
  food.y = HAL_RNG_GetRandomNumber(&hrng) % 8;

  while (self != 0) {
    if (food.x == self->x && food.y == self->y) {
      goto GEN;
    }
    self = self->next;
  }
}

void move() {
  switch (direction) {
  case RIGHT:
    dx = 0;
    dy = 7;
    break;
  case LEFT:
    dx = 0;
    dy = 1;
    break;
  case UP:
    dx = 7;
    dy = 0;
    break;
  case DOWN:
    dx = 1;
    dy = 0;
    break;
  default:
    break;
  }

  dot_t *self = ptr;

  uint8_t next_x, next_y, last_x, last_y, head_x, head_y;
  next_x = self->x + dx;
  next_y = self->y + dy;

  if (next_x >= 8) next_x -= 8;

  if (next_y >= 8) next_y -= 8;

  head_x = next_x, head_y = next_y;

  if (next_x == food.x && next_y == food.y) {
    dot_t *node = make_dot();
    node->x = food.x, node->y = food.y, node->next = self;
    ptr = node;
    generate_food();
    return;
  }

  while (self != 0) {
    last_x = self->x;
    last_y = self->y;
    self->x = next_x;
    self->y = next_y;
    next_x = last_x;
    next_y = last_y;
    if (head_x == next_x && head_y == next_y) {
      display();
      game_over();
    }
    self = self->next;
  }
}

void game_over() {
  max7219(Shutdown, 0);
  HAL_NVIC_SystemReset();
}

