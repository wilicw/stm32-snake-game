void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
  disable_key = false;
  switch (key) {
  case RIGHT:
    if (direction == LEFT)
      disable_key = true;
    break;
  case LEFT:
    if (direction == RIGHT)
      disable_key = true;
    break;
  case UP:
    if (direction == DOWN)
      disable_key = true;
    break;
  case DOWN:
    if (direction == UP)
      disable_key = true;
    break;
  default:
    break;
  }
  if (!disable_key)
    direction = key;
  HAL_UART_Receive_IT(&huart2, &key, 1);
}

